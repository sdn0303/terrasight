export function useTranslation() {
  const locale = useUIStore((s) => s.locale);
  return {
    t: (key: string): string => messages[locale]?.[key] ?? key,
    locale,
  };
}

export function t(locale: Locale, key: string): string {
  return messages[locale]?.[key] ?? key;
}
